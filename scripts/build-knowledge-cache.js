#!/usr/bin/env node
'use strict';

/**
 * build-knowledge-cache.js — Parse les PDFs de knowledge/ et écrit un cache .md
 *
 * Pourquoi un cache ?
 *   `loadKnowledgeBase()` est appelé de manière synchrone au chargement des modules
 *   (messageHandler.js, cron/*.js). Le parsing PDF est async et coûteux. On parse
 *   une fois, on écrit `knowledge/.pdf-cache.md`, et le loader lit ce fichier comme
 *   n'importe quel autre .md.
 *
 * Usage :
 *   node scripts/build-knowledge-cache.js
 *   npm run knowledge:build
 *
 * À relancer chaque fois qu'un PDF est ajouté/modifié.
 */

const fs = require('fs');
const path = require('path');
const { PDFParse } = require('pdf-parse');

const KNOWLEDGE_DIR = path.join(__dirname, '..', 'knowledge');
const CACHE_FILE = path.join(KNOWLEDGE_DIR, '.pdf-cache.md');
const MAX_CHARS_PER_PDF = 25000; // évite de saturer la context window

function cleanText(raw) {
  return raw
    .replace(/\r\n/g, '\n')
    .replace(/\n{3,}/g, '\n\n')
    .replace(/[ \t]+\n/g, '\n')
    .replace(/\u0000/g, '')
    .trim();
}

function truncate(text, max) {
  if (text.length <= max) return text;
  return text.slice(0, max) + `\n\n[... tronqué à ${max} caractères pour économiser le contexte LLM ...]`;
}

async function parsePdf(filePath) {
  const buf = fs.readFileSync(filePath);
  const parser = new PDFParse({ data: buf });
  const result = await parser.getText();
  return {
    text: cleanText(result.text || ''),
    pages: result.total || (result.pages?.length ?? 0)
  };
}

async function main() {
  if (!fs.existsSync(KNOWLEDGE_DIR)) {
    console.error(`❌ ${KNOWLEDGE_DIR} not found`);
    process.exit(1);
  }

  const pdfs = fs.readdirSync(KNOWLEDGE_DIR)
    .filter(f => f.toLowerCase().endsWith('.pdf'))
    .sort();

  if (!pdfs.length) {
    console.log('Aucun PDF dans knowledge/. Rien à faire.');
    if (fs.existsSync(CACHE_FILE)) fs.unlinkSync(CACHE_FILE);
    return;
  }

  console.log(`📚 Parsing ${pdfs.length} PDF(s) depuis knowledge/...\n`);

  const sections = [];
  let totalChars = 0;

  for (const name of pdfs) {
    const full = path.join(KNOWLEDGE_DIR, name);
    const started = Date.now();
    try {
      const { text, pages } = await parsePdf(full);
      const truncated = truncate(text, MAX_CHARS_PER_PDF);
      totalChars += truncated.length;

      sections.push(
        `### ${name.replace(/\.pdf$/i, '')}\n` +
        `_Source PDF : ${name} (${pages} pages, ${text.length} chars extracted, ${truncated.length} used)_\n\n` +
        truncated
      );

      console.log(`  ✓ ${name} — ${pages}p, ${text.length} chars → ${truncated.length} kept (${((Date.now() - started) / 1000).toFixed(1)}s)`);
    } catch (err) {
      console.error(`  ✗ ${name} — ${err.message}`);
    }
  }

  const header = `<!-- AUTO-GENERATED by scripts/build-knowledge-cache.js — do not edit manually -->\n<!-- Generated: ${new Date().toISOString()} -->\n\n`;
  const body = sections.join('\n\n---\n\n');
  fs.writeFileSync(CACHE_FILE, header + body, 'utf8');

  console.log(`\n✅ Cache écrit : ${path.relative(process.cwd(), CACHE_FILE)}`);
  console.log(`   ${sections.length} PDFs, ${totalChars} chars total`);
  console.log(`\n💡 Le prochain démarrage du bot (ou script d'article) utilisera automatiquement ce cache.`);
}

main().catch(err => {
  console.error('\n❌ Erreur :', err.message);
  process.exit(1);
});
